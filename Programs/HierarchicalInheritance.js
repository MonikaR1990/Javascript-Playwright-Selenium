class Animal
{
    eat()
    {
        console.log("Eating...")
    }
}

class Dog extends Animal
{
    bark()
    {
        console.log("Barking...")
    }
}

class Cat extends Animal
{
    meow()
    {
        console.log("Meowing...")
    }
}

class Lion extends Animal
{
    roar()
    {
        console.log("roaring...")
    }
}

class puppy extends Dog
{
    play()
    {
        console.log("playing...")
    }
}

